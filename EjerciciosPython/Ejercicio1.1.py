print("Introduce un número del 0 a 10  \n")
numero = int(input())
if numero == 9 :
    print("A \n")
elif numero == 10 :
    print ("A \n")
elif numero == 8 :
    print ("B \n")
elif numero == 7 :
    print ("C \n")
elif numero == 6 :
    print ("D \n")
elif numero < 6 :
    print ("E \n")
else:
    print("Has insertado un número fuera del rango \n")