print("Introduce una vocal \n")

vocal = input()

def vocales(vocal):
    if vocal == "a":
        print("True \n")
    elif vocal == "e":
        print ("True \n")
    elif vocal == "i":
        print ("True \n")
    elif vocal == "o":
        print ("True \n")
    elif vocal == "u":
        print ("True \n")
    else:
        print ("False \n")

vocales(vocal)